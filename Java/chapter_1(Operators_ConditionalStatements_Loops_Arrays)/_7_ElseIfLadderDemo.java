
public class _7_ElseIfLadderDemo 
{

	public static void main(String[] args) 
	{
		int marks=75;
		if(marks>=90)
		{
			System.out.println("Grade- A");
		}
		else if(marks>=80)
		{
			System.out.println("Grade- B");
		}
		else if(marks>=70)
		{
			System.out.println("Grade- C");
		}
		else if(marks>=35)
		{
			System.out.println("Grade- D");
		}
		else
		{
			System.out.println("Fail");
		}

	}

}
